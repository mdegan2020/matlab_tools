function count = matchCount(result)
count = 0;
if isstruct(result) && isfield(result, "Matches") && ~isempty(result.Matches)
    count = sum([result.Matches.Count]);
end
end

function artifacts = writeArtifacts(summary, outputDirectory)
if ~isfolder(outputDirectory)
    mkdir(outputDirectory);
end
jsonPath = fullfile(outputDirectory, "alignment_reliability_summary.json");
writeText(jsonPath, jsonencode(summary, PrettyPrint=true));
matPath = fullfile(outputDirectory, "alignment_reliability_summary.mat");
save(matPath, "summary");

detectors = summary.Detectors;
detectorTable = table(string({detectors.Detector}).', ...
    [detectors.ExactRepeat].', [detectors.RawCount].', ...
    [detectors.FilteredCount].', [detectors.RejectedCount].', ...
    [detectors.TerrainMedianMeters].', [detectors.TerrainP95Meters].', ...
    [detectors.TotalSeconds].', VariableNames=["Detector", "ExactRepeat", ...
    "RawCount", "FilteredCount", "RejectedCount", ...
    "TerrainMedianMeters", "TerrainP95Meters", "TotalSeconds"]);
detectorPath = fullfile(outputDirectory, "detector_matrix.csv");
writetable(detectorTable, detectorPath);

losses = summary.Losses;
lossTable = table(string({losses.Name}).', string({losses.LossMode}).', ...
    [losses.AllowReferenceMotion].', [losses.UnequalPriors].', ...
    string({losses.Status}).', [losses.Actionable].', ...
    [losses.AnyBoundHit].', [losses.PlaneRmsBefore].', ...
    [losses.PlaneRmsAfter].', [losses.RayRmsBefore].', ...
    [losses.RayRmsAfter].', [losses.CoplanarityRmsBefore].', ...
    [losses.CoplanarityRmsAfter].', [losses.ObservedRank].', ...
    [losses.ModeCount].', [losses.WeakModeCount].', ...
    [losses.TotalSeconds].', string({losses.FailureReason}).', ...
    VariableNames=["Name", "LossMode", "AllowReferenceMotion", ...
    "UnequalPriors", "Status", "Actionable", "AnyBoundHit", ...
    "PlaneRmsBefore", "PlaneRmsAfter", "RayRmsBefore", ...
    "RayRmsAfter", "CoplanarityRmsBefore", "CoplanarityRmsAfter", ...
    "ObservedRank", "ModeCount", "WeakModeCount", "TotalSeconds", ...
    "FailureReason"]);
lossPath = fullfile(outputDirectory, "loss_matrix.csv");
writetable(lossTable, lossPath);

regressions = summary.ContractRegressions;
regressionTable = table(string({regressions.Category}).', ...
    string({regressions.TestName}).', string({regressions.Status}).', ...
    [regressions.Passed].', [regressions.Failed].', ...
    [regressions.Incomplete].', [regressions.DurationSeconds].', ...
    VariableNames=["Category", "TestName", "Status", "Passed", ...
    "Failed", "Incomplete", "DurationSeconds"]);
regressionPath = fullfile(outputDirectory, "contract_regression_matrix.csv");
writetable(regressionTable, regressionPath);
artifacts = struct(OutputDirectory=string(outputDirectory), ...
    SummaryPath=string(jsonPath), MatPath=string(matPath), ...
    DetectorMatrixPath=string(detectorPath), ...
    LossMatrixPath=string(lossPath), ...
    RegressionMatrixPath=string(regressionPath));
end

function writeText(filePath, value)
fileId = fopen(filePath, "w");
if fileId < 0
    error("alignment_reliability_validation:fileWriteFailed", ...
        "Unable to open %s for writing.", filePath);
end
cleanup = onCleanup(@() fclose(fileId));
fprintf(fileId, "%s", value);
clear cleanup
end

function printSummary(summary, artifacts)
fprintf("Alignment reliability validation: %s\n", artifacts.OutputDirectory);
fprintf("Selected detector: %s\n", summary.SelectedDetector);
for record = summary.Detectors
    fprintf("  %s raw=%d filtered=%d repeat=%d truth p95=%.3g m\n", ...
        record.Detector, record.RawCount, record.FilteredCount, ...
        record.ExactRepeat, record.TerrainP95Meters);
end
for record = summary.Losses
    fprintf("  %s status=%s safe=%s ray %.3g -> %.3g\n", ...
        record.Name, record.Status, record.SafetyStatus, ...
        record.RayRmsBefore, record.RayRmsAfter);
end
if ~isempty(summary.ContractRegressions)
    fprintf("Contract regressions: %d passed, %d failed, %d incomplete\n", ...
        nnz([summary.ContractRegressions.Passed]), ...
        nnz([summary.ContractRegressions.Failed]), ...
        nnz([summary.ContractRegressions.Incomplete]));
end
end

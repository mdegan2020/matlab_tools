function results = benchmark_dense_association()
%benchmark_dense_association Measure privacy-safe indexed association scaling.

root = fileparts(fileparts(mfilename("fullpath")));
addpath(fullfile(root, "src"), fullfile(root, "tests"));
counts = [64 250 500 5000];
viewCounts = [2 5];
ProjectionDenseObservationAssociator.associate( ...
    ProjectionDenseAssociationFixture.benchmarkRequest(8, 2));
results = repmat(struct(ViewCount=0, RecordsPerPair=0, PairCount=0, ...
    TotalRecordCount=0, ElapsedSeconds=0, TotalIndexedWorkCount=0, ...
    TrackCount=0, ObservationCount=0), 1, ...
    numel(viewCounts) * numel(counts));
cursor = 0;
for viewCount = viewCounts
    pairCount = viewCount * (viewCount - 1) / 2;
    for recordsPerPair = counts
        cursor = cursor + 1;
        request = ProjectionDenseAssociationFixture. ...
            benchmarkRequest(recordsPerPair, viewCount);
        started = tic;
        association = ProjectionDenseObservationAssociator.associate(request);
        elapsed = toc(started);
        results(cursor) = struct(ViewCount=viewCount, ...
            RecordsPerPair=recordsPerPair, PairCount=pairCount, ...
            TotalRecordCount=numel(request.Records), ...
            ElapsedSeconds=elapsed, TotalIndexedWorkCount= ...
            association.Diagnostics.TotalIndexedWorkCount, ...
            TrackCount=association.Diagnostics.TrackCount, ...
            ObservationCount=association.Diagnostics.ObservationCount);
    end
end
fprintf("Association benchmark on %s, MATLAB %s\n", ...
    string(computer), string(version));
fprintf("Reported MATLAB compute cores: %d\n", feature("numcores"));
disp(struct2table(results));
end

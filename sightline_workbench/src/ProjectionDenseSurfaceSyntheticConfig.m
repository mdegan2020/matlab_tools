        function config = validateGimbal(config)
            gimbal = config.gimbal;
            gimbal.zero_boresight_axis = ...
                ProjectionDenseSurfaceSyntheticConfig.enumText( ...
                gimbal.zero_boresight_axis, "gimbal.zero_boresight_axis", ...
                "platform-positive-z");
            gimbal.rotation_order = ...
                ProjectionDenseSurfaceSyntheticConfig.textVector( ...
                gimbal.rotation_order, "gimbal.rotation_order");
            if ~isequal(gimbal.rotation_order, ["roll" "pitch"])
                error("ProjectionDenseSurfaceSyntheticConfig:invalidGimbal", ...
                    "gimbal.rotation_order must be roll followed by pitch.");
            end
            gimbal.roll_axis = ProjectionDenseSurfaceSyntheticConfig.enumText( ...
                gimbal.roll_axis, "gimbal.roll_axis", "platform-positive-x");
            gimbal.pitch_axis = ProjectionDenseSurfaceSyntheticConfig.enumText( ...
                gimbal.pitch_axis, "gimbal.pitch_axis", ...
                "roll-gimbal-positive-y");
            gimbal.positive_rotation_convention = ...
                ProjectionDenseSurfaceSyntheticConfig.enumText( ...
                gimbal.positive_rotation_convention, ...
                "gimbal.positive_rotation_convention", "right-hand");
            gimbal.pitch_field_of_regard_degrees = ...
                ProjectionDenseSurfaceSyntheticConfig.increasingPair( ...
                gimbal.pitch_field_of_regard_degrees, ...
                "gimbal.pitch_field_of_regard_degrees");
            if any(gimbal.pitch_field_of_regard_degrees <= -89) || ...
                    any(gimbal.pitch_field_of_regard_degrees >= 89)
                error("ProjectionDenseSurfaceSyntheticConfig:invalidGimbal", ...
                    "The pitch field of regard must remain between -89 and 89 degrees.");
            end
            gimbal.maximum_initial_forward_pitch_degrees = ...
                ProjectionDenseSurfaceSyntheticConfig.nonnegativeScalar( ...
                gimbal.maximum_initial_forward_pitch_degrees, ...
                "gimbal.maximum_initial_forward_pitch_degrees");
            if gimbal.maximum_initial_forward_pitch_degrees > ...
                    gimbal.pitch_field_of_regard_degrees(2)
                error("ProjectionDenseSurfaceSyntheticConfig:invalidGimbal", ...
                    "Maximum initial forward pitch must not exceed the upper pitch limit.");
            end
            gimbal.ideal_encoder_and_mounting = ...
                ProjectionDenseSurfaceSyntheticConfig.logicalScalar( ...
                gimbal.ideal_encoder_and_mounting, ...
                "gimbal.ideal_encoder_and_mounting");
            if ~gimbal.ideal_encoder_and_mounting
                error("ProjectionDenseSurfaceSyntheticConfig:invalidGimbal", ...
                    "The first fixture requires ideal gimbal encoders and mounting.");
            end
            config.gimbal = gimbal;
        end


classdef ProjectionDenseSurfaceSyntheticConfig
    %ProjectionDenseSurfaceSyntheticConfig Load and validate synthetic-fixture configuration.

    properties (Constant)
        SchemaVersion = 1
    end

    methods (Static)
        function [config, context] = load(filePath)
            %load Read a JSON configuration and resolve its runtime paths.
            filePath = ProjectionDenseSurfaceSyntheticConfig.scalarText( ...
                filePath, "filePath");
            if ~isfile(filePath)
                error("ProjectionDenseSurfaceSyntheticConfig:fileNotFound", ...
                    "Synthetic fixture configuration does not exist: %s", filePath);
            end

            try
                config = jsondecode(fileread(filePath));
            catch exception
                error("ProjectionDenseSurfaceSyntheticConfig:invalidJson", ...
                    "Synthetic fixture configuration is not valid JSON: %s", ...
                    exception.message);
            end
            config = ProjectionDenseSurfaceSyntheticConfig.validate(config);

            configPath = string(filePath);
            configDirectory = string(fileparts(configPath));
            projectRoot = string(fileparts(configDirectory));
            sourceImagePath = ProjectionDenseSurfaceSyntheticConfig.resolvePath( ...
                projectRoot, config.fixture.source_image_path);
            if ~isfile(sourceImagePath)
                error("ProjectionDenseSurfaceSyntheticConfig:sourceImageNotFound", ...
                    "Configured source image does not exist relative to the project root.");
            end
            outputDirectory = ProjectionDenseSurfaceSyntheticConfig.resolvePath( ...
                projectRoot, config.output.directory);
            context = struct(ConfigPath=configPath, ProjectRoot=projectRoot, ...
                SourceImagePath=sourceImagePath, OutputDirectory=outputDirectory);
        end

        function config = validate(config)
            %validate Enforce the complete committed schema and semantic contract.
            template = ProjectionDenseSurfaceSyntheticConfig.loadTemplate();
            ProjectionDenseSurfaceSyntheticConfig.validateStructure( ...
                config, template, "configuration");

            config.schema_version = ...
                ProjectionDenseSurfaceSyntheticConfig.positiveInteger( ...
                config.schema_version, "schema_version");
            if config.schema_version ~= ...
                    ProjectionDenseSurfaceSyntheticConfig.SchemaVersion
                error("ProjectionDenseSurfaceSyntheticConfig:unsupportedVersion", ...
                    "schema_version must be %d.", ...
                    ProjectionDenseSurfaceSyntheticConfig.SchemaVersion);
            end

            config = ProjectionDenseSurfaceSyntheticConfig.validateFixture(config);
            config = ProjectionDenseSurfaceSyntheticConfig.validateImage(config);
            config = ProjectionDenseSurfaceSyntheticConfig.validateFrames(config);
            config = ProjectionDenseSurfaceSyntheticConfig.validatePlatform(config);
            config = ProjectionDenseSurfaceSyntheticConfig.validateGimbal(config);
            config = ProjectionDenseSurfaceSyntheticConfig.validatePlanner(config);
            config = ProjectionDenseSurfaceSyntheticConfig.validateSampling(config);
            config = ProjectionDenseSurfaceSyntheticConfig.validateTerrain(config);
            config = ProjectionDenseSurfaceSyntheticConfig.validateTurbulence(config);
            config = ProjectionDenseSurfaceSyntheticConfig.validateNavigation(config);
            config = ProjectionDenseSurfaceSyntheticConfig.validateAcceptance(config);
            config = ProjectionDenseSurfaceSyntheticConfig.validateOutput(config);
            config = ProjectionDenseSurfaceSyntheticConfig.validateFuture(config);
        end
    end

    methods (Static, Access = private)
        function template = loadTemplate()
            sourceDirectory = string(fileparts(mfilename("fullpath")));
            templatePath = fullfile(fileparts(sourceDirectory), "config", ...
                "dense_surface_synthetic.template.json");
            template = jsondecode(fileread(templatePath));
        end

        function validateStructure(value, template, path)
            if ~isstruct(template)
                if isempty(value)
                    error("ProjectionDenseSurfaceSyntheticConfig:missingValue", ...
                        "%s must not be null or empty.", path);
                end
                return
            end
            if ~isstruct(value) || ~isscalar(value)
                error("ProjectionDenseSurfaceSyntheticConfig:invalidStructure", ...
                    "%s must be a JSON object.", path);
            end
            expected = sort(string(fieldnames(template)));
            actual = sort(string(fieldnames(value)));
            if ~isequal(expected, actual)
                error("ProjectionDenseSurfaceSyntheticConfig:schemaMismatch", ...
                    "%s fields must match the committed template exactly.", path);
            end
            names = fieldnames(template);
            for fieldIndex = 1:numel(names)
                name = names{fieldIndex};
                ProjectionDenseSurfaceSyntheticConfig.validateStructure( ...
                    value.(name), template.(name), path + "." + name);
            end
        end


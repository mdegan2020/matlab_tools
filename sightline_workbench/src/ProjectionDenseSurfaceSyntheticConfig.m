        function value = resolvePath(baseDirectory, value)
            value = string(value);
            isAbsolute = startsWith(value, string(filesep)) || ...
                ~isempty(regexp(char(value), "^[A-Za-z]:[\\\\/]", "once"));
            if ~isAbsolute
                value = string(fullfile(baseDirectory, value));
            end
        end

        function value = enumText(value, name, allowed)
            value = ProjectionDenseSurfaceSyntheticConfig.scalarText(value, name);
            if ~ismember(value, string(allowed))
                error("ProjectionDenseSurfaceSyntheticConfig:invalidValue", ...
                    "%s has an unsupported value.", name);
            end
        end

        function value = scalarText(value, name)
            value = string(value);
            if ~isscalar(value) || ismissing(value) || strlength(value) == 0
                error("ProjectionDenseSurfaceSyntheticConfig:invalidText", ...
                    "%s must be a nonempty scalar string.", name);
            end
        end

        function value = textVector(value, name)
            value = string(value(:).');
            if isempty(value) || any(ismissing(value)) || any(strlength(value) == 0)
                error("ProjectionDenseSurfaceSyntheticConfig:invalidText", ...
                    "%s must be a nonempty string vector.", name);
            end
        end

        function value = finiteScalar(value, name)
            if ~isnumeric(value) || ~isscalar(value) || ~isfinite(value)
                error("ProjectionDenseSurfaceSyntheticConfig:invalidNumber", ...
                    "%s must be a finite numeric scalar.", name);
            end
            value = double(value);
        end

        function value = positiveScalar(value, name)
            value = ProjectionDenseSurfaceSyntheticConfig.finiteScalar(value, name);
            if value <= 0
                error("ProjectionDenseSurfaceSyntheticConfig:invalidNumber", ...
                    "%s must be positive.", name);
            end
        end

        function value = nonnegativeScalar(value, name)
            value = ProjectionDenseSurfaceSyntheticConfig.finiteScalar(value, name);
            if value < 0
                error("ProjectionDenseSurfaceSyntheticConfig:invalidNumber", ...
                    "%s must be nonnegative.", name);
            end
        end

        function value = openRangeScalar(value, name, minimum, maximum)
            value = ProjectionDenseSurfaceSyntheticConfig.finiteScalar(value, name);
            if value <= minimum || value >= maximum
                error("ProjectionDenseSurfaceSyntheticConfig:invalidNumber", ...
                    "%s must be greater than %g and less than %g.", ...
                    name, minimum, maximum);
            end
        end

        function value = positiveInteger(value, name, minimum)
            if nargin < 3
                minimum = 1;
            end
            value = ProjectionDenseSurfaceSyntheticConfig.finiteScalar(value, name);
            if value < minimum || fix(value) ~= value
                error("ProjectionDenseSurfaceSyntheticConfig:invalidInteger", ...
                    "%s must be an integer greater than or equal to %d.", ...
                    name, minimum);
            end
        end

        function value = nonnegativeInteger(value, name)
            value = ProjectionDenseSurfaceSyntheticConfig.positiveInteger( ...
                value, name, 0);
        end

        function value = finiteVector(value, name)
            if ~isnumeric(value) || isempty(value) || ~isvector(value) || ...
                    any(~isfinite(value))
                error("ProjectionDenseSurfaceSyntheticConfig:invalidVector", ...
                    "%s must be a nonempty finite numeric vector.", name);
            end
            value = double(value(:).');
        end

        function value = positiveVector(value, name)
            value = ProjectionDenseSurfaceSyntheticConfig.finiteVector(value, name);
            if any(value <= 0)
                error("ProjectionDenseSurfaceSyntheticConfig:invalidVector", ...
                    "%s values must be positive.", name);
            end
        end

        function value = positiveIntegerVector(value, name)
            value = ProjectionDenseSurfaceSyntheticConfig.positiveVector(value, name);
            if any(fix(value) ~= value)
                error("ProjectionDenseSurfaceSyntheticConfig:invalidVector", ...
                    "%s values must be positive integers.", name);
            end
        end

        function value = increasingPair(value, name)
            value = ProjectionDenseSurfaceSyntheticConfig.finiteVector(value, name);
            if numel(value) ~= 2 || value(1) >= value(2)
                error("ProjectionDenseSurfaceSyntheticConfig:invalidVector", ...
                    "%s must be a strictly increasing two-element vector.", name);
            end
        end

        function value = logicalScalar(value, name)
            if ~islogical(value) || ~isscalar(value)
                error("ProjectionDenseSurfaceSyntheticConfig:invalidLogical", ...
                    "%s must be a logical scalar.", name);
            end
        end

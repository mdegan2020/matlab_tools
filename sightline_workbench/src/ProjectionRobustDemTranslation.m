classdef ProjectionRobustDemTranslation < ProjectionSurfaceRegistrationAlgorithm
    %ProjectionRobustDemTranslation Robust covariance-weighted ENU translation.

    methods
        function metadata = metadata(~)
            metadata = struct( ...
                AlgorithmId="sightline.registration.robust-dem-translation", ...
                Name="Robust DEM global translation", ...
                SemanticVersion="1.0.0", ...
                Capabilities=struct(PointToSurfaceNormal=true, ...
                PointCovariance=true, DemAccuracy=true, ...
                MaskSensitivity=true, ProposedCorrection=true), ...
                AllowedTransform="globalTranslation", Deterministic=true, ...
                Precision="double", CpuSupported=true, GpuSupported=false);
        end

        function options = defaultOptions(~)
            options = struct(InitialTranslationEnuMeters=zeros(3, 1), ...
                MaximumStepMeters=25, MinimumVarianceMetersSquared=1e-8, ...
                MinimumHuberScaleMeters=0.05, RankTolerance=1e-10, ...
                CovarianceScaleFloor=1e-6, ...
                AmbiguityConditionLimit=1e8, ...
                MaskSensitivityToleranceMeters=1);
        end

        function options = validateOptions(algorithm, options)
            defaults = algorithm.defaultOptions();
            if isempty(options)
                options = struct();
            end
            if ~isstruct(options) || ~isscalar(options)
                error("ProjectionRobustDemTranslation:invalidOptions", ...
                    "Options must be a scalar struct.");
            end
            names = string(fieldnames(options));
            unknown = setdiff(names, string(fieldnames(defaults)));
            if ~isempty(unknown)
                error("ProjectionRobustDemTranslation:invalidOptions", ...
                    "Unexpected option: %s.", unknown(1));
            end
            for name = names.'
                defaults.(name) = options.(name);
            end
            translation = defaults.InitialTranslationEnuMeters;
            if ~isnumeric(translation) || numel(translation) ~= 3 || ...
                    any(~isfinite(translation))
                error("ProjectionRobustDemTranslation:invalidOptions", ...
                    "InitialTranslationEnuMeters must be finite 3x1.");
            end
            defaults.InitialTranslationEnuMeters = reshape( ...
                double(translation), 3, 1);
            positive = ["MaximumStepMeters" ...
                "MinimumVarianceMetersSquared" "MinimumHuberScaleMeters" ...
                "RankTolerance" "CovarianceScaleFloor" ...
                "AmbiguityConditionLimit" "MaskSensitivityToleranceMeters"];
            for field = positive
                value = defaults.(field);
                if ~isnumeric(value) || ~isscalar(value) || ...
                        ~isfinite(value) || value <= 0
                    error("ProjectionRobustDemTranslation:invalidOptions", ...
                        "%s must be a positive scalar.", field);
                end
                defaults.(field) = double(value);
            end
            options = defaults;
        end
    end

    methods (Access = protected)
        function result = registerImpl(~, request, options, runtimeControl)
            main = ProjectionSurfaceRegistrationSupport.solve( ...
                request, options, false, runtimeControl);
            ProjectionRobustDemTranslation.publishProgress( ...
                runtimeControl, 0.88, "maskSensitivity");
            sensitivity = ProjectionRobustDemTranslation.sensitivity( ...
                request, options, main, runtimeControl);
            accepted = main.FinalEvaluation.Usable;
            inputCount = numel(main.PointIds);
            acceptedCount = nnz(accepted);
            rejectedCount = inputCount - acceptedCount;
            viewIds = ProjectionRobustDemTranslation. ...
                unionAccepted(main.Points, accepted, "ContributingViewIds");
            passIds = ProjectionRobustDemTranslation. ...
                unionAccepted(main.Points, accepted, "ContributingPassIds");
            support = struct(InputPointCount=inputCount, ...
                CandidatePointCount=nnz(main.CandidateMask), ...
                AcceptedPointCount=acceptedCount, ...
                RejectedPointCount=rejectedCount, ...
                CoverageFraction=acceptedCount / inputCount, ...
                IndependentViewCount=numel(viewIds), ...
                IndependentPassCount=numel(passIds), ...
                RobustDownweightedCount= ...
                main.FinalEvaluation.RobustDownweightedCount);
            ambiguousReasons = strings(1, 0);
            if ~main.Converged
                ambiguousReasons(end + 1) = "notConverged";
            end
            if main.NormalMatrixRank < 3
                ambiguousReasons(end + 1) = "translationNotFullyObservable";
            elseif main.ConditionNumber > options.AmbiguityConditionLimit
                ambiguousReasons(end + 1) = "weakNormalGeometry";
            end
            if sensitivity.Evaluated && ~sensitivity.MaskStable
                ambiguousReasons(end + 1) = "maskSensitive";
            end
            status = "succeeded";
            failure = struct(Valid=true, Code="none", Explanation="none");
            if acceptedCount < request.MinimumSupport || main.NormalMatrixRank < 3
                status = "degenerate";
                failure = struct(Valid=false, Code="insufficientObservability", ...
                    Explanation="Support or surface-normal diversity is insufficient.");
            elseif ~isempty(ambiguousReasons)
                status = "ambiguous";
            end
            worldRotation = request.Dem.Transforms.EnuToProjectWorldRotation;
            translationWorld = worldRotation * main.TranslationEnuMeters;
            covarianceWorld = worldRotation * ...
                main.CovarianceEnuMetersSquared * worldRotation.';
            transform = struct(Kind="globalTranslation", ...
                Direction="imageryToDem", Frame="sceneLocalENU", ...
                TranslationEnuMeters=main.TranslationEnuMeters, ...
                TranslationWorldMeters=translationWorld, RotationMatrix=eye(3));
            covariance = struct(TranslationEnuMetersSquared= ...
                main.CovarianceEnuMetersSquared, ...
                TranslationWorldMetersSquared=covarianceWorld, ...
                Status=main.CovarianceStatus, Reason=main.CovarianceReason, ...
                SharedDemAccuracyIncluded=true);
            registeredWorld = main.OriginalPointsWorld + translationWorld;
            demDifference = main.FinalEvaluation.ResidualsMeters;
            preview = struct(SourcePointSetGenerationId= ...
                request.PointSet.GenerationId, PointIds=main.PointIds, ...
                OriginalPointsWorld=main.OriginalPointsWorld, ...
                RegisteredPointsWorld=registeredWorld, ...
                DemDifferenceMeters=demDifference, AcceptedMask=accepted, ...
                CompleteImageryProductPreserved=true, ...
                AppliedToSourceGeometry=false);
            correctionData = ProjectionSurfaceRegistrationSupport. ...
                correctionData(request, transform, covariance, ...
                "sightline.registration.robust-dem-translation", ...
                main.ConditionNumber, support, failure);
            correctionData.Diagnostics.RegistrationStatus = status;
            correctionData.Diagnostics.Ambiguous = status == "ambiguous";
            correctionData = ProjectionCorrectionSet.create( ...
                correctionData).toStruct();
            ambiguity = struct(IsAmbiguous=status ~= "succeeded", ...
                Reasons=ambiguousReasons, ...
                NormalMatrixRank=main.NormalMatrixRank, ...
                NormalMatrixConditionNumber=main.ConditionNumber, ...
                GaugeOrDatumConfounding= ...
                main.NormalMatrixRank < 3 || ...
                main.ConditionNumber > options.AmbiguityConditionLimit);
            result = struct(Status=status, Transform=transform, ...
                Covariance=covariance, Support=support, ...
                Rejections=main.Rejections, Residuals=struct( ...
                Initial=ProjectionSurfaceRegistrationSupport.residualSummary( ...
                main.InitialEvaluation.ResidualsMeters), ...
                Final=ProjectionSurfaceRegistrationSupport.residualSummary( ...
                main.FinalEvaluation.ResidualsMeters)), ...
                Sensitivity=sensitivity, Datum=struct( ...
                HeightReferenceInput=request.Dem.HeightReferenceInput, ...
                HeightReferenceWorking=request.Dem.HeightReferenceWorking, ...
                GeoidModel=request.Dem.GeoidModel, ...
                Assumption=request.Dem.DatumAssumption, ...
                Accuracy=request.Dem.Accuracy), Ambiguity=ambiguity, ...
                Preview=preview, CorrectionSetData=correctionData, ...
                Diagnostics=struct(Iterations=main.IterationCount, ...
                Converged=main.Converged, ...
                LastStepMeters=main.LastStepMeters, ...
                PointResidualsAreIndependentValidation=false, ...
                IndividualPointsSnappedToDem=false, AutoApplied=false), ...
                Memory=struct(Bytes= ...
                ProjectionSurfaceRegistrationSupport.bytes(main)), ...
                Execution=struct(Device="cpu"), Failure=failure);
        end
    end

    methods (Static, Access = private)
        function sensitivity = sensitivity( ...
                request, options, main, runtimeControl)
            sensitivity = struct(Evaluated=false, ...
                UnmaskedTranslationEnuMeters=nan(3, 1), ...
                DifferenceNormMeters=NaN, MaskStable=false, ...
                ToleranceMeters=options.MaskSensitivityToleranceMeters, ...
                Reason="disabled");
            if ~request.EvaluateMaskSensitivity
                return
            end
            unmasked = ProjectionSurfaceRegistrationSupport.solve( ...
                request, options, true, runtimeControl);
            difference = norm(unmasked.TranslationEnuMeters - ...
                main.TranslationEnuMeters);
            sensitivity = struct(Evaluated=true, ...
                UnmaskedTranslationEnuMeters= ...
                unmasked.TranslationEnuMeters, ...
                DifferenceNormMeters=difference, ...
                MaskStable=difference <= ...
                options.MaskSensitivityToleranceMeters, ...
                ToleranceMeters=options.MaskSensitivityToleranceMeters, ...
                Reason="maskedVersusUnmaskedRegistration");
        end

        function values = unionAccepted(points, accepted, field)
            values = strings(1, 0);
            selected = points(accepted);
            for index = 1:numel(selected)
                values = [values reshape(string( ...
                    selected(index).(field)), 1, [])]; %#ok<AGROW>
            end
            values = sort(unique(values));
        end

        function publishProgress(runtimeControl, fraction, stage)
            if ~isempty(runtimeControl.ProgressFcn)
                runtimeControl.ProgressFcn(struct( ...
                    Fraction=double(fraction), Stage=string(stage)));
            end
        end
    end
end

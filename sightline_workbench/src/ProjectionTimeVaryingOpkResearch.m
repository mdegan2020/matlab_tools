classdef ProjectionTimeVaryingOpkResearch
    %ProjectionTimeVaryingOpkResearch A7 tangent-spline observability study.

    properties (Constant)
        Format = "ProjectionTimeVaryingOpkResearchResult"
        Version = 1
        NominalPostSpacingPixels = 128
    end

    methods (Static)
        function result = analyze(request, options)
            %analyze Fit a research-only pass/view tangent-space spline model.
            if nargin < 2
                options = struct();
            end
            request = ProjectionTimeVaryingOpkResearch.validateRequest(request);
            options = ProjectionTimeVaryingOpkResearch.validateOptions(options);
            timer = tic;
            spacing = options.PostSpacingPixels;
            history = struct("PostSpacingPixels", {}, ...
                "ParameterCount", {}, "ObservationCount", {}, ...
                "DataRank", {}, "RequiredDataRank", {}, ...
                "DataConditionNumber", {}, ...
                "MinimumControlSupport", {}, "SupportSufficient", {}, ...
                "RankSufficient", {}, "ConditionSufficient", {});
            while true
                system = ProjectionTimeVaryingOpkResearch.system( ...
                    request, spacing, options);
                assessment = ProjectionTimeVaryingOpkResearch.assess( ...
                    system, options);
                history(end + 1) = assessment; %#ok<AGROW>
                if options.Mode == "perColumnAnalysis" || ...
                        (assessment.SupportSufficient && ...
                        assessment.RankSufficient && ...
                        assessment.ConditionSufficient) || ...
                        spacing >= options.MaximumPostSpacingPixels
                    break
                end
                spacing = min(options.MaximumPostSpacingPixels, 2 * spacing);
            end
            solution = ProjectionTimeVaryingOpkResearch.solve(system, options);
            views = ProjectionTimeVaryingOpkResearch.viewResults( ...
                request, system, solution);
            passes = ProjectionTimeVaryingOpkResearch.passResults( ...
                system, solution);
            predicted = system.Design * solution.Parameters;
            residual = system.Observations - predicted;
            locallyObservable = assessment.SupportSufficient && ...
                assessment.RankSufficient && assessment.ConditionSufficient;
            if options.Mode == "perColumnAnalysis"
                status = "analysisUpperBound";
            elseif locallyObservable
                status = "locallyObservableResearch";
            else
                status = "insufficientLocalObservability";
            end
            result = struct(Format= ...
                ProjectionTimeVaryingOpkResearch.Format, ...
                Version=ProjectionTimeVaryingOpkResearch.Version, ...
                Status=status, Stage="timeVaryingOpkResearch", ...
                SourceGenerationId=request.SourceGenerationId, ...
                Model=struct(Parameterization="localRotationVector", ...
                Composition="leftComposeExpMapWithNominalRotation", ...
                Basis="openUniformCubicBSpline", Degree=3, ...
                NominalPostSpacingPixels= ...
                ProjectionTimeVaryingOpkResearch.NominalPostSpacingPixels, ...
                SelectedPostSpacingPixels=spacing, ...
                Smoothness="secondDifference", ...
                PassCommonComponent=true, PerImageComponent=true, ...
                PerColumnAnalysisOnly=options.Mode == "perColumnAnalysis"), ...
                Passes=passes, Views=views, ...
                Observability=struct(LocallyObservable=locallyObservable, ...
                History=history, Final=assessment, ...
                PriorAugmentedRank=solution.AugmentedRank, ...
                PriorDominated=~locallyObservable, ...
                Coarsened=spacing > options.PostSpacingPixels), ...
                Residuals=ProjectionTimeVaryingOpkResearch. ...
                residualSummary(residual), ...
                Covariance=struct(Available=solution.CovarianceAvailable, ...
                ParameterRadiansSquared=solution.Covariance, ...
                Status=solution.CovarianceStatus, ...
                Reason=solution.CovarianceReason), ...
                Decision="retainResearchOnly", ...
                DecisionReason= ...
                "Physical dense-network local observability and stability are not yet demonstrated.", ...
                ApplicationSupported=false, AutoApplied=false, ...
                Precision="double", Timing=struct(TotalSeconds=toc(timer)), ...
                Provenance=struct(Method="weightedLinearizedTangentSpline", ...
                TruthUsedOperationally=false, Deterministic=true, ...
                EvidenceKinds=unique(string({request.Views.EvidenceKind})), ...
                Options=options));
            result = ProjectionTimeVaryingOpkResearch.validateResult(result);
        end

        function [basis, definition] = basis(imageWidthPixels, columns, spacing)
            %basis Evaluate an open-uniform cubic B-spline over source columns.
            imageWidthPixels = ProjectionTimeVaryingOpkResearch. ...
                positiveInteger(imageWidthPixels, "ImageWidthPixels");
            spacing = ProjectionTimeVaryingOpkResearch.positiveInteger( ...
                spacing, "PostSpacingPixels");
            if ~isnumeric(columns) || isempty(columns) || ~isvector(columns) || ...
                    any(~isfinite(columns)) || any(columns < 1) || ...
                    any(columns > imageWidthPixels)
                error("ProjectionTimeVaryingOpkResearch:invalidColumns", ...
                    "Source columns must be finite and within the image width.");
            end
            columns = reshape(double(columns), 1, []);
            segmentCount = max(1, ceil((imageWidthPixels - 1) / spacing));
            controlCount = segmentCount + 3;
            knots = [zeros(1, 4) 1:(segmentCount - 1) ...
                segmentCount * ones(1, 4)];
            parameter = (columns - 1) / max(imageWidthPixels - 1, 1) * ...
                segmentCount;
            degreeZero = zeros(numel(columns), controlCount + 3);
            for index = 1:(controlCount + 3)
                degreeZero(:, index) = parameter >= knots(index) & ...
                    parameter < knots(index + 1);
            end
            degreeZero(parameter == segmentCount, controlCount) = 1;
            current = degreeZero;
            for degree = 1:3
                next = zeros(size(current));
                for index = 1:(controlCount + 3 - degree)
                    leftDenominator = knots(index + degree) - knots(index);
                    rightDenominator = ...
                        knots(index + degree + 1) - knots(index + 1);
                    if leftDenominator > 0
                        next(:, index) = next(:, index) + ...
                            ((parameter - knots(index)) / leftDenominator).' .* ...
                            current(:, index);
                    end
                    if rightDenominator > 0
                        next(:, index) = next(:, index) + ...
                            ((knots(index + degree + 1) - parameter) / ...
                            rightDenominator).' .* current(:, index + 1);
                    end
                end
                current = next;
            end
            basis = current(:, 1:controlCount);
            basis(parameter == segmentCount, :) = 0;
            basis(parameter == segmentCount, controlCount) = 1;
            definition = struct(Degree=3, Knots=knots, ...
                SegmentCount=segmentCount, ControlCount=controlCount, ...
                ImageWidthPixels=imageWidthPixels, ...
                PostSpacingPixels=spacing, ...
                ControlAbscissaPixels=linspace(1, imageWidthPixels, controlCount));
        end

        function rotations = compose(nominalRotation, rotationVectors)
            %compose Left-compose tangent rotation vectors with nominal attitude.
            if ~isnumeric(nominalRotation) || ...
                    ~isequal(size(nominalRotation), [3 3]) || ...
                    any(~isfinite(nominalRotation), "all") || ...
                    norm(nominalRotation.' * nominalRotation - eye(3), ...
                    "fro") > 1e-9 || abs(det(nominalRotation) - 1) > 1e-9
                error("ProjectionTimeVaryingOpkResearch:invalidRotation", ...
                    "Nominal attitude must be a proper 3-by-3 rotation.");
            end
            if ~isnumeric(rotationVectors) || ...
                    size(rotationVectors, 1) ~= 3 || ...
                    any(~isfinite(rotationVectors), "all")
                error("ProjectionTimeVaryingOpkResearch:invalidRotationVector", ...
                    "Rotation vectors must be a finite 3-by-N array.");
            end
            nominalRotation = double(nominalRotation);
            rotationVectors = double(rotationVectors);
            rotations = zeros(3, 3, size(rotationVectors, 2));
            for index = 1:size(rotationVectors, 2)
                vector = rotationVectors(:, index);
                angle = norm(vector);
                skew = [0 -vector(3) vector(2); vector(3) 0 -vector(1); ...
                    -vector(2) vector(1) 0];
                if angle < 1e-10
                    increment = eye(3) + skew + 0.5 * skew * skew;
                else
                    increment = eye(3) + sin(angle) / angle * skew + ...
                        (1 - cos(angle)) / angle ^ 2 * (skew * skew);
                end
                rotations(:, :, index) = increment * nominalRotation;
            end
        end


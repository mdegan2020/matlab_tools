                    string(result.Format) ~= ...
                    ProjectionTimeVaryingOpkResearch.Format || ...
                    ~isequal(result.Version, ...
                    ProjectionTimeVaryingOpkResearch.Version) || ...
                    string(result.Stage) ~= "timeVaryingOpkResearch" || ...
                    ~ismember(string(result.Status), [ ...
                    "locallyObservableResearch" ...
                    "insufficientLocalObservability" ...
                    "analysisUpperBound"]) || ...
                    ~isstruct(result.Views) || isempty(result.Views) || ...
                    ~isstruct(result.Passes) || isempty(result.Passes) || ...
                    ~isequal(result.ApplicationSupported, false) || ...
                    ~isequal(result.AutoApplied, false) || ...
                    string(result.Precision) ~= "double" || ...
                    string(result.Decision) ~= "retainResearchOnly" || ...
                    ProjectionTimeVaryingOpkResearch.hasRuntimeValue(result)
                error("ProjectionTimeVaryingOpkResearch:invalidResult", ...
                    "Time-varying OPK research result is invalid.");
            end
        end

        function views = correctionViews(scene, resultViews)
            template = struct(ViewId="", PassId="", LayerId="", ...
                ParentAttitudeRadians=zeros(1, 3), ...
                EffectiveAttitudeRadians=zeros(1, 3), ...
                CommonAttitudeRadians=zeros(1, 3), ...
                DifferentialAttitudeRadians=zeros(1, 3), ...
                ParentRotationMatrix=eye(3), ...
                IncrementRotationMatrix=eye(3), ...
                EffectiveRotationMatrix=eye(3), ...
                IncrementRotationVectorRadians=zeros(1, 3), ...
                ParentProjectionOffsetMeters=zeros(1, 2), ...
                EffectiveProjectionOffsetMeters=zeros(1, 2), ...
                BaseGeometryFingerprint="", ...
                ParentGeometryFingerprint="", ...
                CorrectedGeometryFingerprint="");
            views = repmat(template, 1, numel(resultViews));
            for index = 1:numel(resultViews)
                resultView = resultViews(index);
                layerIndex = ProjectionViewMetadata.indexForId( ...
                    scene, resultView.ViewId);
                layer = scene.layers(layerIndex);
                if string(layer.PassId) ~= string(resultView.PassId)
                    error("ProjectionTimeVaryingOpkResearch:passMismatch", ...
                        "Research result pass does not match the scene.");
                end
                [rotation, radians] = ProjectionMeshBuilder. ...
                    viewVectorRotation(layer, layer.CurrentProjectionPlane);
                fingerprint = ProjectionGeometryFingerprint.layer(layer);
                views(index) = struct(ViewId=string(layer.ViewId), ...
                    PassId=string(layer.PassId), ...
                    LayerId=string(layer.LayerId), ...
                    ParentAttitudeRadians=reshape(radians, 1, 3), ...
                    EffectiveAttitudeRadians=reshape(radians, 1, 3), ...
                    CommonAttitudeRadians=zeros(1, 3), ...
                    DifferentialAttitudeRadians=zeros(1, 3), ...
                    ParentRotationMatrix=rotation, ...
                    IncrementRotationMatrix=eye(3), ...
                    EffectiveRotationMatrix=rotation, ...
                    IncrementRotationVectorRadians=zeros(1, 3), ...
                    ParentProjectionOffsetMeters=reshape(double( ...
                    layer.ProjectionOffsetMeters), 1, 2), ...
                    EffectiveProjectionOffsetMeters=reshape(double( ...
                    layer.ProjectionOffsetMeters), 1, 2), ...
                    BaseGeometryFingerprint=fingerprint, ...
                    ParentGeometryFingerprint=fingerprint, ...
                    CorrectedGeometryFingerprint=fingerprint);
            end
        end

        function blocks = correctionBlocks(result)
            template = struct(Name="", Type="", Scope="", TargetId="", ...
                Values=[], Units="", Frame="", Semantics="");
            blocks = repmat(template, 1, ...
                numel(result.Passes) + numel(result.Views));
            cursor = 0;
            for pass = result.Passes
                cursor = cursor + 1;
                blocks(cursor) = struct(Name="pass common tangent rotation", ...
                    Type="passCommonRotationVector", Scope="pass", ...
                    TargetId=pass.PassId, ...
                    Values=pass.CommonAttitudeRadians, Units="radians", ...
                    Frame="localAttitudeTangent", ...
                    Semantics="leftComposeWithNominalRotation");
            end
            for view = result.Views
                cursor = cursor + 1;
                blocks(cursor) = struct(Name="time-varying OPK spline", ...
                    Type="timeVaryingRotationSpline", Scope="view", ...
                    TargetId=view.ViewId, ...
                    Values=view.DifferentialControlRotationVectorsRadians, ...
                    Units="radians", Frame="localAttitudeTangent", ...
                    Semantics="openUniformCubicByFullSourceColumnResearchOnly");
            end
        end

        function summary = residualSummary(values)
            values = double(values(:));
            absolute = sort(abs(values));
            summary = struct(Count=numel(values), ...
                Rms=sqrt(mean(values .^ 2)), Median=median(values), ...
                P95Absolute=absolute(max(1, ceil(0.95 * numel(absolute)))), ...
                MaximumAbsolute=max(absolute));
        end

        function value = positiveInteger(value, name)
            if ~isnumeric(value) || ~isscalar(value) || ~isfinite(value) || ...
                    value < 1 || fix(value) ~= value
                error("ProjectionTimeVaryingOpkResearch:invalidInteger", ...
                    "%s must be a positive integer.", name);
            end
            value = double(value);
        end

        function value = requiredString(value, name)
            value = string(value);
            if ~isscalar(value) || ismissing(value) || ...
                    strlength(value) == 0 || value ~= strip(value)
                error("ProjectionTimeVaryingOpkResearch:invalidString", ...
                    "%s must be a nonempty trimmed string.", name);
            end
        end

        function tf = hasRuntimeValue(value)
            if isa(value, "function_handle") || isa(value, "handle")
                tf = true;
            elseif isstruct(value)
                tf = false;
                names = fieldnames(value);
                for element = 1:numel(value)
                    for index = 1:numel(names)
                        if ProjectionTimeVaryingOpkResearch.hasRuntimeValue( ...
                                value(element).(names{index}))
                            tf = true;
                            return
                        end
                    end
                end
            elseif iscell(value)
                tf = any(cellfun(@ProjectionTimeVaryingOpkResearch. ...
                    hasRuntimeValue, value));
            else
                tf = false;
            end
        end
    end

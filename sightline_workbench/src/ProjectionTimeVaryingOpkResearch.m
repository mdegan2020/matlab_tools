    methods (Static, Access = private)
        function request = validateRequest(request)
            if ~isstruct(request) || ~isscalar(request) || ...
                    any(~isfield(request, ["SourceGenerationId" "Views"])) || ...
                    isfield(request, "Truth") || isfield(request, "Expected") || ...
                    ProjectionTimeVaryingOpkResearch.hasRuntimeValue(request)
                error("ProjectionTimeVaryingOpkResearch:invalidRequest", ...
                    "Research request must be portable and truth-free.");
            end
            request.SourceGenerationId = ...
                ProjectionTimeVaryingOpkResearch.requiredString( ...
                request.SourceGenerationId, "SourceGenerationId");
            if ~isstruct(request.Views) || numel(request.Views) < 2
                error("ProjectionTimeVaryingOpkResearch:invalidViews", ...
                    "At least two view observation records are required.");
            end
            required = ["ViewId" "PassId" "ImageWidthPixels" ...
                "SourceColumnsPixels" "RotationJacobians" "Residuals" ...
                "Weights" "EvidenceKind"];
            viewIds = strings(1, numel(request.Views));
            for index = 1:numel(request.Views)
                view = request.Views(index);
                if any(~isfield(view, required))
                    error("ProjectionTimeVaryingOpkResearch:invalidViews", ...
                        "View observation schema is incomplete.");
                end
                view.ViewId = ProjectionTimeVaryingOpkResearch. ...
                    requiredString(view.ViewId, "ViewId");
                view.PassId = ProjectionTimeVaryingOpkResearch. ...
                    requiredString(view.PassId, "PassId");
                view.ImageWidthPixels = ProjectionTimeVaryingOpkResearch. ...
                    positiveInteger(view.ImageWidthPixels, "ImageWidthPixels");
                columns = reshape(double(view.SourceColumnsPixels), [], 1);
                residuals = reshape(double(view.Residuals), [], 1);
                weights = reshape(double(view.Weights), [], 1);
                count = numel(columns);
                if count < 3 || ~isnumeric(view.SourceColumnsPixels) || ...
                        any(~isfinite(columns)) || any(columns < 1) || ...
                        any(columns > view.ImageWidthPixels) || ...
                        ~isnumeric(view.RotationJacobians) || ...
                        ~isequal(size(view.RotationJacobians), [count 3]) || ...
                        any(~isfinite(view.RotationJacobians), "all") || ...
                        numel(residuals) ~= count || any(~isfinite(residuals)) || ...
                        numel(weights) ~= count || any(~isfinite(weights)) || ...
                        any(weights <= 0)
                    error("ProjectionTimeVaryingOpkResearch:invalidObservations", ...
                        "Columns, Jacobians, residuals, and weights are inconsistent.");
                end
                evidence = string(view.EvidenceKind);
                if ~isscalar(evidence) || ...
                        ~ismember(evidence, ["sparse" "dense" "mixed"])
                    error("ProjectionTimeVaryingOpkResearch:invalidEvidence", ...
                        "EvidenceKind must be sparse, dense, or mixed.");
                end
                view.SourceColumnsPixels = columns;
                view.RotationJacobians = double(view.RotationJacobians);
                view.Residuals = residuals;
                view.Weights = weights;
                view.EvidenceKind = evidence;
                request.Views(index) = view;
                viewIds(index) = view.ViewId;
            end
            if numel(unique(viewIds)) ~= numel(viewIds)
                error("ProjectionTimeVaryingOpkResearch:duplicateView", ...
                    "ViewId values must be unique.");
            end
        end

        function options = validateOptions(options)
            defaults = struct(PostSpacingPixels= ...
                ProjectionTimeVaryingOpkResearch.NominalPostSpacingPixels, ...
                MaximumPostSpacingPixels=2048, ...
                MinimumObservationsPerControl=12, ...
                MaximumDataConditionNumber=1e10, ...
                MaximumParameterCount=2000, ...
                SmoothnessWeight=1e-3, GaugeWeight=1e3, Mode="spline");
            if isempty(options)
                options = struct();
            end
            if ~isstruct(options) || ~isscalar(options)
                error("ProjectionTimeVaryingOpkResearch:invalidOptions", ...
                    "Options must be a scalar struct.");
            end
            names = string(fieldnames(options));
            unknown = setdiff(names, string(fieldnames(defaults)));
            if ~isempty(unknown)
                error("ProjectionTimeVaryingOpkResearch:invalidOptions", ...
                    "Unexpected option %s.", unknown(1));
            end
            for name = names.'
                defaults.(name) = options.(name);
            end
            defaults.PostSpacingPixels = ...
                ProjectionTimeVaryingOpkResearch.positiveInteger( ...
                defaults.PostSpacingPixels, "PostSpacingPixels");
            defaults.MaximumPostSpacingPixels = ...
                ProjectionTimeVaryingOpkResearch.positiveInteger( ...
                defaults.MaximumPostSpacingPixels, ...
                "MaximumPostSpacingPixels");
            defaults.MinimumObservationsPerControl = ...
                ProjectionTimeVaryingOpkResearch.positiveInteger( ...
                defaults.MinimumObservationsPerControl, ...
                "MinimumObservationsPerControl");
            defaults.MaximumParameterCount = ...
                ProjectionTimeVaryingOpkResearch.positiveInteger( ...
                defaults.MaximumParameterCount, "MaximumParameterCount");
            scalars = ["MaximumDataConditionNumber" "SmoothnessWeight" ...
                "GaugeWeight"];
            for name = scalars
                value = defaults.(name);
                if ~isnumeric(value) || ~isscalar(value) || ...
                        ~isfinite(value) || value <= 0
                    error("ProjectionTimeVaryingOpkResearch:invalidOptions", ...
                        "%s must be positive and finite.", name);
                end
                defaults.(name) = double(value);
            end
            defaults.Mode = string(defaults.Mode);
            if ~isscalar(defaults.Mode) || ...
                    ~ismember(defaults.Mode, ["spline" "perColumnAnalysis"])
                error("ProjectionTimeVaryingOpkResearch:invalidOptions", ...
                    "Mode must be spline or perColumnAnalysis.");
            end
            if defaults.Mode == "perColumnAnalysis"
                defaults.PostSpacingPixels = 1;
                defaults.MaximumPostSpacingPixels = 1;
            elseif defaults.MaximumPostSpacingPixels < ...
                    defaults.PostSpacingPixels
                error("ProjectionTimeVaryingOpkResearch:invalidOptions", ...
                    "Maximum post spacing cannot be smaller than initial spacing.");
            end
            options = defaults;
        end

        function system = system(request, spacing, options)
            passIds = unique(string({request.Views.PassId}), "stable");
            passOffsets = containers.Map(cellstr(passIds), ...
                num2cell(1 + 3 * (0:numel(passIds)-1)));
            cursor = 3 * numel(passIds) + 1;
            layouts = repmat(struct(ViewId="", PassId="", Offset=0, ...
                ControlCount=0, Basis=zeros(0), Definition=struct(), ...
                ObservationIndices=[]), 1, numel(request.Views));
            observationCount = sum(arrayfun( ...
                @(view) numel(view.Residuals), request.Views));
            for index = 1:numel(request.Views)
                view = request.Views(index);
                [basis, definition] = ProjectionTimeVaryingOpkResearch. ...
                    basis(view.ImageWidthPixels, ...
                    view.SourceColumnsPixels, spacing);
                layouts(index) = struct(ViewId=view.ViewId, ...
                    PassId=view.PassId, Offset=cursor, ...
                    ControlCount=definition.ControlCount, Basis=basis, ...
                    Definition=definition, ObservationIndices=[]);
                cursor = cursor + 3 * definition.ControlCount;
            end
            parameterCount = cursor - 1;
            if parameterCount > options.MaximumParameterCount
                error("ProjectionTimeVaryingOpkResearch:resourceLimit", ...
                    "The requested model has %d parameters; the bounded " + ...
                    "research limit is %d.", parameterCount, ...
                    options.MaximumParameterCount);
            end
            design = zeros(observationCount, parameterCount);
            observations = zeros(observationCount, 1);
            weights = zeros(observationCount, 1);
            row = 0;
            for index = 1:numel(request.Views)
                view = request.Views(index);
                count = numel(view.Residuals);
                rows = row + (1:count);
                layouts(index).ObservationIndices = rows;
                passOffset = passOffsets(char(view.PassId));
                design(rows, passOffset:(passOffset + 2)) = ...
                    view.RotationJacobians;
                for control = 1:layouts(index).ControlCount
                    columns = layouts(index).Offset + 3 * (control - 1) + (0:2);
                    design(rows, columns) = ...
                        view.RotationJacobians .* ...
                        layouts(index).Basis(:, control);
                end
                observations(rows) = view.Residuals;
                weights(rows) = view.Weights;
                row = row + count;
            end
            prior = ProjectionTimeVaryingOpkResearch.prior( ...
                layouts, passIds, parameterCount, options);
            controlSupport = zeros(1, 0);
            for layout = layouts
                controlSupport = [controlSupport ...
                    sum(layout.Basis > 1e-8, 1)]; %#ok<AGROW>
            end
            system = struct(Design=design, Observations=observations, ...
                Weights=weights, Prior=prior, Layouts=layouts, ...
                PassIds=passIds, PassOffsets=passOffsets, ...
                ParameterCount=parameterCount, ...
                ControlSupport=controlSupport, PostSpacingPixels=spacing);
        end

        function prior = prior(layouts, passIds, count, options)
            rows = cell(1, 0);
            for layout = layouts
                if layout.ControlCount >= 3
                    difference = diff(eye(layout.ControlCount), 2, 1);
                    for component = 1:3
                        block = zeros(size(difference, 1), count);
                        columns = layout.Offset + component - 1 + ...
                            3 * (0:(layout.ControlCount - 1));
                        block(:, columns) = ...
                            sqrt(options.SmoothnessWeight) * difference;
                        rows{end + 1} = block; %#ok<AGROW>
                    end
                end
            end
            for passId = passIds
                selected = layouts(string({layouts.PassId}) == passId);
                for component = 1:3
                    block = zeros(1, count);
                    for layout = selected
                        columns = layout.Offset + component - 1 + ...
                            3 * (0:(layout.ControlCount - 1));
                        block(columns) = sqrt(options.GaugeWeight) / ...
                            (numel(selected) * layout.ControlCount);
                    end
                    rows{end + 1} = block; %#ok<AGROW>
                end
            end
            if isempty(rows)
                prior = zeros(0, count);
            else
                prior = vertcat(rows{:});
            end
        end

        function assessment = assess(system, options)
            weighted = sqrt(system.Weights) .* system.Design;
            singularValues = svd(weighted, "econ");
            tolerance = max(size(weighted)) * eps(max(singularValues));
            dataRank = nnz(singularValues > tolerance);
            requiredRank = system.ParameterCount - 3 * numel(system.PassIds);
            conditionNumber = Inf;
            if dataRank >= requiredRank && requiredRank > 0
                conditionNumber = singularValues(1) / ...
                    singularValues(requiredRank);
            end
            minimumSupport = min(system.ControlSupport);
            assessment = struct(PostSpacingPixels= ...
                system.PostSpacingPixels, ...
                ParameterCount=system.ParameterCount, ...
                ObservationCount=size(system.Design, 1), DataRank=dataRank, ...
                RequiredDataRank=requiredRank, ...
                DataConditionNumber=conditionNumber, ...
                MinimumControlSupport=minimumSupport, ...
                SupportSufficient=minimumSupport >= ...
                options.MinimumObservationsPerControl, ...
                RankSufficient=dataRank >= requiredRank, ...
                ConditionSufficient=conditionNumber <= ...
                options.MaximumDataConditionNumber);
        end

        function solution = solve(system, ~)
            weightedDesign = sqrt(system.Weights) .* system.Design;
            weightedObservations = sqrt(system.Weights) .* system.Observations;
            augmented = [weightedDesign; system.Prior];
            rightHandSide = [weightedObservations; ...
                zeros(size(system.Prior, 1), 1)];
            parameters = augmented \ rightHandSide;
            normal = augmented.' * augmented;
            augmentedRank = rank(augmented);
            covarianceAvailable = augmentedRank == system.ParameterCount;
            if covarianceAvailable
                covariance = pinv(normal);
                covarianceStatus = "researchLinearized";
                covarianceReason = "weightedSplineNormalMatrix";
            else
                covariance = zeros(0);
                covarianceStatus = "unavailable";
                covarianceReason = "rankDeficientResearchSystem";
            end
            solution = struct(Parameters=parameters, ...
                AugmentedRank=augmentedRank, Covariance=covariance, ...
                CovarianceAvailable=covarianceAvailable, ...
                CovarianceStatus=covarianceStatus, ...
                CovarianceReason=covarianceReason);
        end

        function views = viewResults(request, system, solution)
            template = struct(ViewId="", PassId="", EvidenceKind="", ...
                ImageWidthPixels=0, PostSpacingPixels=0, ...
                ControlAbscissaPixels=zeros(1, 0), ...
                DifferentialControlRotationVectorsRadians=zeros(0, 3), ...
                SourceColumnsPixels=zeros(1, 0), ...
                EffectiveRotationVectorsAtObservationsRadians=zeros(0, 3), ...
                LocalSupportCounts=zeros(1, 0));
            views = repmat(template, 1, numel(request.Views));
            for index = 1:numel(request.Views)
                view = request.Views(index);
                layout = system.Layouts(index);
                coefficients = reshape(solution.Parameters( ...
                    layout.Offset:(layout.Offset + ...
                    3 * layout.ControlCount - 1)), 3, []).';
                passOffset = system.PassOffsets(char(view.PassId));
                common = solution.Parameters(passOffset:(passOffset + 2)).';
                effective = common + layout.Basis * coefficients;
                views(index) = struct(ViewId=view.ViewId, ...
                    PassId=view.PassId, EvidenceKind=view.EvidenceKind, ...
                    ImageWidthPixels=view.ImageWidthPixels, ...
                    PostSpacingPixels=system.PostSpacingPixels, ...
                    ControlAbscissaPixels= ...
                    layout.Definition.ControlAbscissaPixels, ...
                    DifferentialControlRotationVectorsRadians=coefficients, ...
                    SourceColumnsPixels=reshape( ...
                    view.SourceColumnsPixels, 1, []), ...
                    EffectiveRotationVectorsAtObservationsRadians=effective, ...
                    LocalSupportCounts=sum(layout.Basis > 1e-8, 1));
            end
        end

        function passes = passResults(system, solution)
            passes = repmat(struct(PassId="", ...
                CommonAttitudeRadians=zeros(1, 3), ...
                TranslationMeters=zeros(1, 3), ...
                TranslationAvailable=false), 1, numel(system.PassIds));
            for index = 1:numel(system.PassIds)
                passId = system.PassIds(index);
                offset = system.PassOffsets(char(passId));
                passes(index).PassId = passId;
                passes(index).CommonAttitudeRadians = ...
                    solution.Parameters(offset:(offset + 2)).';
            end
        end


        function priors = validatePointingPriors(priors)
            defaults = struct();
            defaults.DefaultSigmaDegrees = [1 1 1];
            defaults.LayerIds = strings(1, 0);
            defaults.SigmaDegrees = zeros(0, 3);
            priors = ProjectionAlignmentOptions.mergeStruct(defaults, priors, ...
                "PointingPriors");
            priors.DefaultSigmaDegrees = ...
                ProjectionAlignmentOptions.validatePositiveVector( ...
                priors.DefaultSigmaDegrees, 3, ...
                "PointingPriors.DefaultSigmaDegrees");
            priors.LayerIds = reshape(string(priors.LayerIds), 1, []);
            if any(strlength(priors.LayerIds) == 0) || ...
                    numel(unique(priors.LayerIds)) ~= numel(priors.LayerIds)
                error("ProjectionAlignmentOptions:invalidPointingPriors", ...
                    "PointingPriors.LayerIds must contain unique nonempty IDs.");
            end
            sigma = priors.SigmaDegrees;
            if isempty(sigma)
                sigma = zeros(0, 3);
            end
            if ~isnumeric(sigma) || size(sigma, 2) ~= 3 || ...
                    any(~isfinite(sigma), "all") || any(sigma <= 0, "all") || ...
                    size(sigma, 1) ~= numel(priors.LayerIds)
                error("ProjectionAlignmentOptions:invalidPointingPriors", ...
                    "PointingPriors.SigmaDegrees must be a positive finite N-by-3 array matching LayerIds.");
            end
            priors.SigmaDegrees = double(sigma);
        end


        function policy = validateSafeSolvePolicy(policy)
            if isstruct(policy) && isscalar(policy) && ...
                    isfield(policy, "MinResidualImprovementFraction")
                if ~isfield(policy, "PreferredResidualImprovementFraction")
                    policy.PreferredResidualImprovementFraction = ...
                        policy.MinResidualImprovementFraction;
                end
                policy = rmfield(policy, "MinResidualImprovementFraction");
            end
            defaults = struct();
            defaults.Enabled = true;
            defaults.MinSolverObservationsPerPair = 3;
            defaults.MinPreferredObservationsPerPair = 10;
            defaults.FailOnBoundHit = true;
            defaults.PreferredResidualImprovementFraction = 0.10;
            defaults.MaximumResidualDegradationFraction = 0.01;
            defaults.MaximumConditionNumber = [];
            defaults.MaximumAttitudeStandardDeviationDegrees = [];
            policy = ProjectionAlignmentOptions.mergeStruct(defaults, policy, ...
                "SafeSolvePolicy");
            policy.Enabled = ProjectionAlignmentOptions.validateLogicalScalar( ...
                policy.Enabled, "SafeSolvePolicy.Enabled");
            policy.MinSolverObservationsPerPair = ...
                ProjectionAlignmentOptions.validatePositiveInteger( ...
                policy.MinSolverObservationsPerPair, ...
                "SafeSolvePolicy.MinSolverObservationsPerPair");
            policy.MinPreferredObservationsPerPair = ...
                ProjectionAlignmentOptions.validatePositiveInteger( ...
                policy.MinPreferredObservationsPerPair, ...
                "SafeSolvePolicy.MinPreferredObservationsPerPair");
            if policy.MinPreferredObservationsPerPair < ...
                    policy.MinSolverObservationsPerPair
                error("ProjectionAlignmentOptions:invalidInteger", ...
                    "SafeSolvePolicy.MinPreferredObservationsPerPair must be greater than or equal to MinSolverObservationsPerPair.");
            end
            policy.FailOnBoundHit = ...
                ProjectionAlignmentOptions.validateLogicalScalar( ...
                policy.FailOnBoundHit, "SafeSolvePolicy.FailOnBoundHit");
            policy.PreferredResidualImprovementFraction = ...
                ProjectionAlignmentOptions.validateOptionalNonnegativeFraction( ...
                policy.PreferredResidualImprovementFraction, ...
                "SafeSolvePolicy.PreferredResidualImprovementFraction");
            policy.MaximumResidualDegradationFraction = ...
                ProjectionAlignmentOptions.validateOptionalNonnegativeFraction( ...
                policy.MaximumResidualDegradationFraction, ...
                "SafeSolvePolicy.MaximumResidualDegradationFraction");
            policy.MaximumConditionNumber = ...
                ProjectionAlignmentOptions.validateOptionalPositiveScalar( ...
                policy.MaximumConditionNumber, ...
                "SafeSolvePolicy.MaximumConditionNumber");
            policy.MaximumAttitudeStandardDeviationDegrees = ...
                ProjectionAlignmentOptions.validateOptionalPositiveScalar( ...
                policy.MaximumAttitudeStandardDeviationDegrees, ...
                "SafeSolvePolicy.MaximumAttitudeStandardDeviationDegrees");
        end

        function execution = validateExecution(execution)
            defaults = struct();
            defaults.Mode = "serial";
            defaults.UseGPU = false;
            execution = ProjectionAlignmentOptions.mergeStruct(defaults, execution, ...
                "Execution");
            execution.Mode = ProjectionAlignmentOptions.validateChoice( ...
                execution.Mode, ["serial", "threads"], "Execution.Mode");
            execution.UseGPU = ProjectionAlignmentOptions.validateLogicalScalar( ...
                execution.UseGPU, "Execution.UseGPU");
        end

        function value = validateChoice(value, allowed, name)
            value = ProjectionAlignmentOptions.validateScalarString(value, name);
            matches = lower(value) == lower(allowed);
            if ~any(matches)
                error("ProjectionAlignmentOptions:invalidChoice", ...
                    "%s must be one of: %s.", name, strjoin(allowed, ", "));
            end
            value = allowed(find(matches, 1, "first"));
        end

        function values = validateStringChoices(values, allowed, name)
            values = string(values);
            values = reshape(values, 1, []);
            if isempty(values) || any(strlength(values) == 0)
                error("ProjectionAlignmentOptions:invalidChoice", ...
                    "%s must contain nonempty string values.", name);
            end
            for k = 1:numel(values)
                values(k) = ProjectionAlignmentOptions.validateChoice( ...
                    values(k), allowed, name);
            end
        end

        function value = validateScalarString(value, name)
            if ~(ischar(value) || isstring(value)) || ~isscalar(string(value)) || ...
                    strlength(string(value)) == 0
                error("ProjectionAlignmentOptions:invalidString", ...
                    "%s must be a nonempty scalar string.", name);
            end
            value = string(value);
        end

        function value = validateOptionalScalarString(value, name)
            if isempty(value)
                value = "";
                return
            end
            if ~(ischar(value) || isstring(value)) || ~isscalar(string(value))
                error("ProjectionAlignmentOptions:invalidString", ...
                    "%s must be a scalar string.", name);
            end
            value = string(value);
        end

        function value = validatePositiveScalar(value, name)
            if ~isnumeric(value) || ~isscalar(value) || ~isfinite(value) || value <= 0
                error("ProjectionAlignmentOptions:invalidScalar", ...
                    "%s must be a positive finite scalar.", name);
            end
            value = double(value);
        end

        function value = validateNonnegativeScalar(value, name)
            if ~isnumeric(value) || ~isscalar(value) || ~isfinite(value) || value < 0
                error("ProjectionAlignmentOptions:invalidScalar", ...
                    "%s must be a nonnegative finite scalar.", name);
            end
            value = double(value);
        end

        function value = validateOptionalNonnegativeScalar(value, name)
            if isempty(value)
                value = [];
                return
            end
            value = ProjectionAlignmentOptions.validateNonnegativeScalar(value, name);
        end

        function value = validateOptionalPositiveScalar(value, name)
            if isempty(value)
                value = [];
                return
            end
            value = ProjectionAlignmentOptions.validatePositiveScalar(value, name);
        end

        function value = validateOptionalNonnegativeVector(value, count, name)
            if isempty(value)
                value = [];
                return
            end
            if ~isnumeric(value) || ~(isscalar(value) || numel(value) == count) || ...
                    any(~isfinite(value), "all") || any(value < 0, "all")
                error("ProjectionAlignmentOptions:invalidScalar", ...
                    "%s must be a nonnegative finite scalar or %d-vector.", ...
                    name, count);
            end
            value = double(value(:).');
            if isscalar(value)
                value = repmat(value, 1, count);
            end
        end

        function value = validatePositiveVector(value, count, name)
            if ~isnumeric(value) || numel(value) ~= count || ...
                    any(~isfinite(value), "all") || any(value <= 0, "all")
                error("ProjectionAlignmentOptions:invalidScalar", ...
                    "%s must be a positive finite %d-vector.", name, count);
            end
            value = double(value(:).');
        end

        function value = validateFraction(value, name)
            value = ProjectionAlignmentOptions.validatePositiveScalar(value, name);
            if value > 1
                error("ProjectionAlignmentOptions:invalidScalar", ...
                    "%s must be in the range (0, 1].", name);
            end
        end

        function value = validateOptionalFraction(value, name)
            if isempty(value)
                value = [];
                return
            end
            value = ProjectionAlignmentOptions.validateFraction(value, name);
        end

        function value = validateOptionalPercentage(value, name)
            if isempty(value)
                value = [];
                return
            end
            value = ProjectionAlignmentOptions.validatePositiveScalar(value, name);
            if value > 100
                error("ProjectionAlignmentOptions:invalidScalar", ...
                    "%s must be in the range (0, 100].", name);
            end
        end

        function value = validateOptionalNonnegativeFraction(value, name)
            if isempty(value)
                value = [];
                return
            end
            value = ProjectionAlignmentOptions.validateNonnegativeScalar(value, name);
            if value > 1
                error("ProjectionAlignmentOptions:invalidScalar", ...
                    "%s must be in the range [0, 1].", name);
            end
        end

        function value = validatePositiveInteger(value, name)
            value = ProjectionAlignmentOptions.validatePositiveScalar(value, name);
            if fix(value) ~= value
                error("ProjectionAlignmentOptions:invalidInteger", ...
                    "%s must be a positive integer.", name);
            end
        end

        function value = validateOptionalPositiveInteger(value, name)
            if isempty(value)
                value = [];
                return
            end
            value = ProjectionAlignmentOptions.validatePositiveInteger(value, name);
        end

        function values = validateStringVector(values, name)
            if isempty(values)
                values = strings(1, 0);
                return
            end
            if ~(isstring(values) || iscellstr(values) || ischar(values)) || ...
                    ~isvector(values)
                error("ProjectionAlignmentOptions:invalidString", ...
                    "%s must be a string vector.", name);
            end
            values = reshape(string(values), 1, []);
            if any(ismissing(values)) || any(strlength(strip(values)) == 0) || ...
                    any(values ~= strip(values)) || numel(unique(values)) ~= numel(values)
                error("ProjectionAlignmentOptions:invalidString", ...
                    "%s values must be unique, trimmed, and nonempty.", name);
            end
        end

        function value = validateLogicalScalar(value, name)
            if ~(islogical(value) || isnumeric(value)) || ~isscalar(value)
                error("ProjectionAlignmentOptions:invalidLogical", ...
                    "%s must be a scalar logical value.", name);
            end
            value = logical(value);
        end

        function value = validateRange(value, name)
            if ~isnumeric(value) || numel(value) ~= 2 || any(~isfinite(value), "all")
                error("ProjectionAlignmentOptions:invalidRange", ...
                    "%s must be a finite numeric two-element range.", name);
            end
            value = double(value(:).');
            if value(1) <= 0 || value(2) <= value(1)
                error("ProjectionAlignmentOptions:invalidRange", ...
                    "%s must be positive and strictly increasing.", name);
            end
        end

        function value = validateFilterFcn(value)
            if isempty(value)
                value = [];
                return
            end
            if ~isa(value, "function_handle")
                error("ProjectionAlignmentOptions:invalidFilterFcn", ...
                    "FilterPipeline.RadialFilterFcn must be empty or a function handle.");
            end
        end

        function output = mergeStruct(defaults, overrides, name)
            if isempty(overrides)
                output = defaults;
                return
            end
            if ~isstruct(overrides) || ~isscalar(overrides)
                error("ProjectionAlignmentOptions:invalidOptions", ...
                    "%s must be a scalar struct.", name);
            end

            output = defaults;
            names = fieldnames(overrides);
            for k = 1:numel(names)
                output.(names{k}) = overrides.(names{k});
            end
        end

        function value = fieldOrDefault(value, fieldName, defaultValue)
            if isfield(value, fieldName)
                value = value.(fieldName);
            else
                value = defaultValue;
            end
        end

        function filePath = validateFilePath(filePath)
            if ~(ischar(filePath) || (isstring(filePath) && isscalar(filePath))) || ...
                    strlength(string(filePath)) == 0
                error("ProjectionAlignmentOptions:invalidPath", ...
                    "File path must be a nonempty character vector or scalar string.");
            end
            filePath = char(filePath);
        end

        function writeTextFile(filePath, text)
            filePath = ProjectionAlignmentOptions.validateFilePath(filePath);
            fid = fopen(filePath, "w");
            if fid < 0
                error("ProjectionAlignmentOptions:fileOpenFailed", ...
                    "Unable to open alignment options file for writing: %s", filePath);
            end
            cleaner = onCleanup(@() fclose(fid));
            fprintf(fid, "%s\n", text);
            clear cleaner
        end
    end

                default_image_format="tiff", ...
                supported_image_formats=["tiff" "png"], ...
                preserve_source_integer_class=true, ...
                write_compact_mat_truth=true, write_json_run_summary=true);
            config.future = struct( ...
                reserve_maximum_images_within_for_mode=true, ...
                reserve_degraded_gps_mode=true, ...

                forward_velocity_meters_per_second=50, ...
                nominal_off_nadir_degrees=60, target_side="right");
            config.gimbal = struct(zero_boresight_axis="platform-positive-z", ...
                rotation_order=["roll" "pitch"], ...
                roll_axis="platform-positive-x", ...
                pitch_axis="roll-gimbal-positive-y", ...

                stop_for_user_on_infeasible_geometry=true);
            config.sampling = struct(maximum_oversampling_per_axis=4, ...
                derive_range=true, minimum_reflection_tiles_rows=3, ...
                minimum_reflection_tiles_columns=3, ...
                maximum_reflection_tiles_rows=31, ...
                maximum_reflection_tiles_columns=31, ...
                prefer_odd_tile_counts=true, logical_reflection_addressing=true);
            config.terrain = struct( ...
                model="asymmetric-windowed-radial-sinc-composite", ...

            config = struct();
            config.schema_version = 1;
            config.fixture = struct( ...
                source_image_path="test_data/public_texture.tif", ...
                reserved_source_image_paths="test_data/reserved_texture.tif", ...
                random_seed=17);
            config.image = struct(rows=101, columns=301, view_count=3, ...
                instantaneous_line_axis="rows", scan_axis="columns", ...
                scan_rate_lines_per_second=200, cross_track_fov_degrees=2, ...
                focal_length_meters=0.2, source_band_sequence=[1 2 3]);
            config.world_frame = struct( ...
                x_axis_definition="nominal-flight-forward", ...
                y_axis_definition="right-of-flight", z_axis_definition="up", ...
                platform_x_axis_definition="nose-forward", ...
                platform_y_axis_definition="right-wing", ...
                platform_z_axis_definition="down");

function [scene, truth, comparison, artifacts] = ...
        alignment_oblique_terrain_evaluation(imagePath, options)
%ALIGNMENT_OBLIQUE_TERRAIN_EVALUATION Compare alignment renderers on terrain.
%
% [scene, truth, comparison, artifacts] = ...
%     alignment_oblique_terrain_evaluation(imagePath, options) uses the red
% and blue bands of an RGB test image as independent rectified terrain
% textures. It drapes them over a smooth +/-50 m synthetic DEM, renders two
% 10 km pushbroom views at 65 degrees off nadir and 3 degrees apart in
% azimuth, and compares the alignment working-image radiometry modes against
% known ground-coordinate truth.

projectRoot = fileparts(fileparts(mfilename("fullpath")));
if nargin < 1 || isempty(imagePath)
    imagePath = fullfile(projectRoot, "test_data", "10.tif");
end
if nargin < 2 || isempty(options)
    options = struct();
end
options = mergeOptions(options, projectRoot);

[scene, truth] = ProjectionAlignmentObliqueTerrainHarness.createSceneFromRgbTiff( ...
    imagePath, options.SimulationOptions);
alignmentOptions = ProjectionAlignmentOptions.validate(options.AlignmentOptions);
request = ProjectionAlignmentRequest.validate(struct(Scene=scene, ...
    LayerIndices=[2 1], ReferenceLayerIndex=1, AnalysisBands=[1 1], ...
    Options=alignmentOptions));

comparison = ProjectionAlignmentWorkingImageComparison.evaluate( ...
    scene, request, options.ComparisonOptions);
comparison = ProjectionAlignmentObliqueTerrainHarness.addTruthDiagnostics( ...
    comparison, truth);
comparison.Summary.Simulation = scene.Simulation;

comparisonArtifacts = ...
    ProjectionAlignmentWorkingImageComparison.writeArtifacts( ...
    options.OutputDirectory, comparison);
simulationArtifacts = ...
    ProjectionAlignmentObliqueTerrainHarness.writeSimulationArtifacts( ...
    options.OutputDirectory, scene, truth);
artifacts = struct(OutputDirectory=options.OutputDirectory, ...
    Comparison=comparisonArtifacts, Simulation=simulationArtifacts);

fprintf("Oblique terrain fixture: range=%.0f m, off-nadir=%.1f deg, azimuth separation=%.1f deg\n", ...
    scene.Simulation.RangeMeters, scene.Simulation.OffNadirDegrees, ...
    scene.Simulation.AzimuthSeparationDegrees);
for modeIndex = 1:numel(comparison.Summary.Modes)
    mode = comparison.Summary.Modes(modeIndex);
    rawTruth = mode.TerrainTruth.Raw;
    filteredTruth = mode.TerrainTruth.Filtered;
    format = "%s: raw=%s filtered=%s raw terrain median/p95=%.2f/%.2f m " + ...
        "filtered terrain median/p95=%.2f/%.2f m correction=%.5f deg status=%s\n";
    fprintf(format, ...
        mode.Mode, mat2str(mode.RawMatchCounts), ...
        mat2str(mode.FilteredMatchCounts), ...
        rawTruth.MedianSeparationMeters, rawTruth.P95SeparationMeters, ...
        filteredTruth.MedianSeparationMeters, ...
        filteredTruth.P95SeparationMeters, ...
        mode.Solve.CorrectionNormDegrees, mode.Solve.Status);
end
fprintf("Review artifacts: %s\n", artifacts.OutputDirectory);
end

function options = mergeOptions(options, projectRoot)
if ~isstruct(options) || ~isscalar(options)
    error("alignment_oblique_terrain_evaluation:invalidOptions", ...
        "Options must be a scalar struct.");
end
defaults = struct( ...
    OutputDirectory=fullfile(projectRoot, "artifacts", ...
        "alignment_oblique_terrain_comparison"), ...
    SimulationOptions=struct(), ...
    AlignmentOptions=struct( ...
        Detector=struct(Method="auto", MaxFeatures=2000), ...
        Matcher=struct(MaxRatio=0.9), ...
        FilterPipeline=struct(GeometricMethod="similarity", ...
        NativeDisplacementMethod="mad")), ...
    ComparisonOptions=struct(RenderOptions=struct(OutputSize=[768 768]), ...
        PerturbationDegrees=[0.01 0 0], RunSolve=true));
names = fieldnames(defaults);
for k = 1:numel(names)
    if isfield(options, names{k})
        defaults.(names{k}) = options.(names{k});
    end
end
defaults.OutputDirectory = string(defaults.OutputDirectory);
if ~isscalar(defaults.OutputDirectory) || ...
        strlength(defaults.OutputDirectory) == 0
    error("alignment_oblique_terrain_evaluation:invalidOutput", ...
        "OutputDirectory must be a nonempty scalar path.");
end
if ~isstruct(defaults.SimulationOptions) || ...
        ~isscalar(defaults.SimulationOptions) || ...
        ~isstruct(defaults.AlignmentOptions) || ...
        ~isscalar(defaults.AlignmentOptions) || ...
        ~isstruct(defaults.ComparisonOptions) || ...
        ~isscalar(defaults.ComparisonOptions)
    error("alignment_oblique_terrain_evaluation:invalidOptions", ...
        "Simulation, alignment, and comparison options must be scalar structs.");
end
options = defaults;
end

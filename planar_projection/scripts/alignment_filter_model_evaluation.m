function [summary, artifacts] = ...
        alignment_filter_model_evaluation(imagePath, options)
%ALIGNMENT_FILTER_MODEL_EVALUATION Compare truthful alignment filters.
%
% Runs deterministic similarity, affine, centered coplanarity, and combined
% filters on the oblique-terrain fixture. Reports exact repeats, per-stage
% counts, small-OPK-perturbation count retention, and terrain-truth separation.

projectRoot = fileparts(fileparts(mfilename("fullpath")));
if nargin < 1 || isempty(imagePath)
    imagePath = fullfile(projectRoot, "test_data", "10.tif");
end
if nargin < 2 || isempty(options)
    options = struct();
end
options = mergeOptions(options, projectRoot);

[scene, truth] = ProjectionAlignmentObliqueTerrainHarness.createSceneFromRgbTiff( ...
    imagePath, options.SimulationOptions);
perturbedScene = perturbMovingLayer(scene, options.PerturbationDegrees);
alignmentOptions = ProjectionAlignmentOptions.validate(struct( ...
    Detector=struct(Method="auto", MaxFeatures=options.MaxFeatures), ...
    Matcher=struct(Method="exhaustive", MaxRatio=options.MaxRatio)));
request = ProjectionAlignmentRequest.validate(struct(Scene=scene, ...
    LayerIndices=[2 1], ReferenceLayerIndex=1, AnalysisBands=[1 1], ...
    Options=alignmentOptions));
perturbedRequest = request;
perturbedRequest.Scene = perturbedScene;
working = ProjectionAlignmentWorkingImageRenderer.render( ...
    scene, request, options.RenderOptions);
perturbedWorking = ProjectionAlignmentWorkingImageRenderer.render( ...
    perturbedScene, perturbedRequest, options.RenderOptions);
matches = ProjectionAlignmentFeatureMatcher.match(working, alignmentOptions);
perturbedMatches = ProjectionAlignmentFeatureMatcher.match( ...
    perturbedWorking, alignmentOptions);

variants = filterVariants();
records = repmat(emptyRecord(), 1, numel(variants));
for k = 1:numel(variants)
    filterOptions = ProjectionAlignmentOptions.validate(struct( ...
        FilterPipeline=variants(k).FilterPipeline));
    first = ProjectionAlignmentMatchFilter.filter( ...
        matches, filterOptions, scene);
    repeated = ProjectionAlignmentMatchFilter.filter( ...
        matches, filterOptions, scene);
    perturbed = ProjectionAlignmentMatchFilter.filter( ...
        perturbedMatches, filterOptions, perturbedScene);
    truthMetric = ProjectionAlignmentObliqueTerrainHarness.truthMetrics( ...
        first, truth);

    record = emptyRecord();
    record.Name = variants(k).Name;
    record.FilterPipeline = filterOptions.FilterPipeline;
    record.ExactRepeat = isequaln(first.MatchLedger, repeated.MatchLedger) && ...
        isequal(first.Matches.MatchRecordIndices, ...
        repeated.Matches.MatchRecordIndices);
    record.RawMatchCount = matches.Matches.Count;
    record.FilteredMatchCount = first.Matches.Count;
    record.PerturbedRawMatchCount = perturbedMatches.Matches.Count;
    record.PerturbedFilteredMatchCount = perturbed.Matches.Count;
    record.FilteredCountRetentionFraction = perturbed.Matches.Count / ...
        max(first.Matches.Count, 1);
    record.TerrainTruth = truthMetric;
    record.Diagnostics = first.Diagnostics.FilterPipeline;
    records(k) = record;
end

summary = struct(Format="ProjectionAlignmentFilterModelEvaluation", ...
    Version=1, SourcePath=string(imagePath), Simulation=scene.Simulation, ...
    PerturbationDegrees=options.PerturbationDegrees, ...
    GridKeysEqual=isequal(working.GridKeys, perturbedWorking.GridKeys), ...
    MatcherDiagnostics=matches.Diagnostics, Variants=records);
if ~isfolder(options.OutputDirectory)
    mkdir(options.OutputDirectory);
end
jsonPath = fullfile(options.OutputDirectory, "summary.json");
writeText(jsonPath, jsonencode(summary, PrettyPrint=true));
matPath = fullfile(options.OutputDirectory, "summary.mat");
save(matPath, "summary");
artifacts = struct(OutputDirectory=options.OutputDirectory, ...
    SummaryPath=string(jsonPath), MatPath=string(matPath));

for record = records
    truthMetric = record.TerrainTruth;
    format = "%s: raw=%d filtered=%d perturbed=%d retention=%.3f " + ...
        "truth median/p95=%.2f/%.2f m exact=%d\n";
    fprintf(format, record.Name, record.RawMatchCount, ...
        record.FilteredMatchCount, record.PerturbedFilteredMatchCount, ...
        record.FilteredCountRetentionFraction, ...
        truthMetric.MedianSeparationMeters, ...
        truthMetric.P95SeparationMeters, record.ExactRepeat);
end
fprintf("Filter-model artifacts: %s\n", artifacts.OutputDirectory);
end

function variants = filterVariants()
variants = [ ...
    struct(Name="similarity", FilterPipeline=struct( ...
    Stages=["overlapMask" "geometricOutlier"], ...
    GeometricMethod="similarity", NativeDisplacementMethod="none")), ...
    struct(Name="affine", FilterPipeline=struct( ...
    Stages=["overlapMask" "geometricOutlier"], ...
    GeometricMethod="affine", NativeDisplacementMethod="none")), ...
    struct(Name="coplanarity", FilterPipeline=struct( ...
    Stages=["overlapMask" "epipolarCoplanarity"], ...
    GeometricMethod="none", CoplanarityMethod="robustMad", ...
    NativeDisplacementMethod="none")), ...
    struct(Name="similarityCoplanarity", FilterPipeline=struct( ...
    Stages=["overlapMask" "geometricOutlier" "epipolarCoplanarity"], ...
    GeometricMethod="similarity", CoplanarityMethod="robustMad", ...
    NativeDisplacementMethod="none"))];
end

function record = emptyRecord()
record = struct(Name="", FilterPipeline=struct(), ExactRepeat=false, ...
    RawMatchCount=0, FilteredMatchCount=0, PerturbedRawMatchCount=0, ...
    PerturbedFilteredMatchCount=0, FilteredCountRetentionFraction=NaN, ...
    TerrainTruth=struct(), Diagnostics=struct());
end

function options = mergeOptions(options, projectRoot)
if ~isstruct(options) || ~isscalar(options)
    error("alignment_filter_model_evaluation:invalidOptions", ...
        "Options must be a scalar struct.");
end
defaults = struct(OutputDirectory=fullfile(projectRoot, "artifacts", ...
    "alignment_filter_model_evaluation"), SimulationOptions=struct(), ...
    RenderOptions=struct(OutputSize=[768 768]), ...
    PerturbationDegrees=[0.01 0 0], MaxFeatures=2000, MaxRatio=0.9);
names = fieldnames(defaults);
for k = 1:numel(names)
    if isfield(options, names{k})
        defaults.(names{k}) = options.(names{k});
    end
end
defaults.OutputDirectory = string(defaults.OutputDirectory);
defaults.PerturbationDegrees = double(defaults.PerturbationDegrees(:).');
if ~isscalar(defaults.OutputDirectory) || ...
        strlength(defaults.OutputDirectory) == 0 || ...
        numel(defaults.PerturbationDegrees) ~= 3 || ...
        any(~isfinite(defaults.PerturbationDegrees)) || ...
        ~isnumeric(defaults.MaxFeatures) || ~isscalar(defaults.MaxFeatures) || ...
        fix(defaults.MaxFeatures) ~= defaults.MaxFeatures || ...
        defaults.MaxFeatures < 1 || ~isnumeric(defaults.MaxRatio) || ...
        ~isscalar(defaults.MaxRatio) || ~isfinite(defaults.MaxRatio) || ...
        defaults.MaxRatio <= 0 || defaults.MaxRatio > 1
    error("alignment_filter_model_evaluation:invalidOptions", ...
        "Output, perturbation, feature-count, or ratio option is invalid.");
end
options = defaults;
end

function scene = perturbMovingLayer(scene, perturbationDegrees)
layer = scene.layers(2);
if isfield(layer, "ViewVectorAngularOffsetsDegrees")
    offsets = double(layer.ViewVectorAngularOffsetsDegrees(:));
else
    offsets = zeros(3, 1);
end
layer.ViewVectorAngularOffsetsDegrees = offsets + perturbationDegrees(:);
scene.layers(2) = layer;
end

function writeText(filePath, value)
fileId = fopen(filePath, "w");
if fileId < 0
    error("alignment_filter_model_evaluation:fileWriteFailed", ...
        "Unable to open %s for writing.", filePath);
end
cleanup = onCleanup(@() fclose(fileId));
fprintf(fileId, "%s", value);
clear cleanup
end

% Run the complete reference generator on a concrete pushbroom model.
pb=ToyPb(lat=35,lon=-117,hae=500,hc=705000,r0=599.5,c0=999.5,sp=8,fp=950000);
c=diag([(1.5e-6)^2 (1.5e-6)^2 3^2 ...
        deg2rad(0.002)^2 deg2rad(0.002)^2 deg2rad(0.003)^2]);
s=Sens(@(g) pb.groundToImage(g), ...
       @(u,h) pb.imageToGround(u,h), ...
       c, ...
       @(g,dq) pb.groundToImagePerturbed(g,dq));
cfg=RsmCfg(iid="TOY_PUSHBROOM",im=[1200 2000],h=[0 1200], ...
           ns=[19 19 7],nt=[13 13 5],ng=[5 5 3],mc=100);
p=make_product(s,cfg);
disp(p.summary());
disp(p.pca.fields());
disp(p.ecb.fields());
